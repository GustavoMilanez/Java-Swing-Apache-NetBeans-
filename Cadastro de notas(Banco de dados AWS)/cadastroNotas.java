    private void btnCadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadActionPerformed
        try {
            String sql = "INSERT INTO GustavoBachetaMilanez04 (nome, periodo, disciplina, notas1, notas2) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, txtNom.getText());
            stmt.setInt(2, Integer.parseInt(txtPer.getText()));
            stmt.setString(3, txtDis.getText());
            stmt.setDouble(4, Double.parseDouble(txtNot1.getText()));
            stmt.setDouble(5, Double.parseDouble(txtNot2.getText()));
            stmt.executeUpdate();
            txtLimpar();
            JOptionPane.showMessageDialog(this, "Aluno cadastrado com sucesso!");
        } catch (SQLException e) {
            e.printStackTrace();
            txtLimpar();
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar aluno: " + e.getMessage());
        }
    }//GEN-LAST:event_btnCadActionPerformed

