    private void btnBusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBusActionPerformed
        try {
            String sql = "SELECT * FROM GustavoBachetaMilanez04 WHERE id = ?";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setInt(1, Integer.parseInt(txtId.getText()));
            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                txtNom.setText(rs.getString("nome"));
                txtPer.setText(rs.getString("periodo"));
                txtDis.setText(rs.getString("disciplina"));
                txtNot1.setText(rs.getString("notas1"));
                txtNot2.setText(rs.getString("notas2"));
                double not1B = rs.getDouble("notas1");
                double not2B = rs.getDouble("notas2");
                double media = ((not1B + not2B) / 2);
                lblMedia.setText("Média final: "+media);
                lblMedia.setVisible(true);
                if(media >= 7.0){
                    lblRes.setText("Aprovado");
                    lblRes.setVisible(true);
                }else if(media < 4.0){
                    lblRes.setText("Reprovado");
                    lblRes.setVisible(true);
                }else if(media < 7.0 && media > 3.9){
                    lblRes.setText("Recuperação");
                    lblRes.setVisible(true);
                }else{
                    lblRes.setText("Erro no calculo...");
                    lblRes.setVisible(true);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Aluno não encontrado.");
                txtLimpar();
            }
        } catch (SQLException e) {
            e.printStackTrace();
            txtLimpar();
            JOptionPane.showMessageDialog(this, "Erro ao buscar aluno: " + e.getMessage());
        }
    }//GEN-LAST:event_btnBusActionPerformed

